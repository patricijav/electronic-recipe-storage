-- ### This script is written in a way that you can run it multiple times and it won't complain about tables already existing, etc. ###

-- ### Deleting all of the tables ###
-- Deleting tables (it also deletes the corresponding indexes), if they are present
DROP TABLE IF EXISTS
    users,
    ingredients,
    recipes,
    recipe_steps,
    favorite_recipes,
    recipe_ingredients,
    user_ingredients;

-- ### Creating all of the tables ###
-- 1. Creating the table that will store important user information
CREATE TABLE users (
	user_id SERIAL PRIMARY KEY,
	email VARCHAR(254) NOT NULL UNIQUE,
	first_name VARCHAR(100) NOT NULL,
	last_name VARCHAR(100) NOT NULL,
	password VARCHAR(255) NOT NULL,
    is_admin BOOLEAN NOT NULL DEFAULT FALSE
);
-- 2. Creating the table that will store ingredients that are defined throughout the application
CREATE TABLE ingredients (
    ingredient_id SERIAL PRIMARY KEY,
    user_id INTEGER REFERENCES users(user_id) ON DELETE SET NULL,
    name VARCHAR(255) NOT NULL,
    calories INTEGER NOT NULL,
    is_liquid BOOLEAN NOT NULL DEFAULT FALSE,
    visibility INTEGER NOT NULL DEFAULT 1,
    CONSTRAINT check_visibility CHECK (visibility IN (0, 1, 2))
);
-- 3. Creating the table that will store general recipe information
CREATE TABLE recipes (
    recipe_id SERIAL PRIMARY KEY,
    user_id INTEGER NOT NULL REFERENCES users(user_id) ON DELETE CASCADE,
    name VARCHAR(50) NOT NULL,
    photo_file_name VARCHAR(255),
    visibility INTEGER NOT NULL DEFAULT 0,
    CONSTRAINT check_visibility CHECK (visibility IN (0, 1, 2))
);
-- 4. Creating the table that will store cooking steps for each of the recipes
CREATE TABLE recipe_steps (
    recipe_id INTEGER NOT NULL REFERENCES recipes(recipe_id) ON DELETE CASCADE,
    step_number INTEGER NOT NULL,
    instruction VARCHAR(1000) NOT NULL,
    PRIMARY KEY (recipe_id, step_number)
);
-- 5. Creating the table that will store which user likes which recipes
CREATE TABLE favorite_recipes (
    user_id INTEGER NOT NULL REFERENCES users(user_id) ON DELETE CASCADE,
    recipe_id INTEGER NOT NULL REFERENCES recipes(recipe_id) ON DELETE CASCADE,
    PRIMARY KEY (user_id, recipe_id)
);
-- 6. Creating the table that will store the amount of speficic ingredient in a specific recipe
CREATE TABLE recipe_ingredients (
    recipe_id INTEGER NOT NULL REFERENCES recipes(recipe_id) ON DELETE CASCADE,
    ingredient_id INTEGER NOT NULL REFERENCES ingredients(ingredient_id),
    amount DECIMAL(10, 2) NOT NULL,
    PRIMARY KEY (recipe_id, ingredient_id)
);
-- 7. Creating the table that will store the amount of a specific ingredient the user has at home
CREATE TABLE user_ingredients (
    user_id INTEGER NOT NULL REFERENCES users(user_id) ON DELETE CASCADE,
    ingredient_id INTEGER NOT NULL REFERENCES ingredients(ingredient_id),
    amount DECIMAL(10, 2) NOT NULL,
    PRIMARY KEY (user_id, ingredient_id)
);

-- ### Creating indexes for faster joins on foreign keys ###
-- 1. ingredients has users(user_id) foreign key
CREATE INDEX idx_ingredients_user_id ON ingredients(user_id);
-- 2. recipes has users(user_id) foreign key
CREATE INDEX idx_recipes_user_id ON recipes(user_id);
-- 3. recipe_steps has recipes(recipe_id) foreign key
CREATE INDEX idx_recipe_steps_recipe_id ON recipe_steps(recipe_id);
-- 4. favorite_recipes has users(user_id) and recipes(recipe_id) foreign keys
CREATE INDEX idx_favorite_recipes_user_id ON favorite_recipes(user_id);
CREATE INDEX idx_favorite_recipes_recipe_id ON favorite_recipes(recipe_id);
-- 5. recipe_ingredients has recipes(recipe_id) and ingredients(ingredient_id) foreign keys
CREATE INDEX idx_recipe_ingredients_recipe_id ON recipe_ingredients(recipe_id);
CREATE INDEX idx_recipe_ingredients_ingredient_id ON recipe_ingredients(ingredient_id);
-- 6. user_ingredients has users(user_id) and ingredients(ingredient_id) foreign keys
CREATE INDEX idx_user_ingredients_user_id ON user_ingredients(user_id);
CREATE INDEX idx_user_ingredients_ingredient_id ON user_ingredients(ingredient_id);

